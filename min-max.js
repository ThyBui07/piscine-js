// Create a function named max that takes 2 number arguments and returns the largest of the two.

// Create a function named min which is the same as max, but returns the lowest.


function max(a,b){
    if (a>b){
        return a
    }
    if (b>a){
        return b
    }
}
function min(a,b){
    if (a>b){
        return b
    }
    if (b>a){
        return a
    }
}